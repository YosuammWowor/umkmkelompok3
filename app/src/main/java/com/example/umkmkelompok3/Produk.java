package com.example.umkmkelompok3;

public class Produk {
    public int id;
    public String nama;
    public String deskripsi;
    public String harga;
    public String image;

    public Produk(int id, String nama, String deskripsi, String harga, String image) {
        this.id = id;
        this.nama = nama;
        this.deskripsi = deskripsi;
        this.harga = harga;
        this.image = image;
    }
}
